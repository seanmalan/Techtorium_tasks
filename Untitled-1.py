
# Creating a string
my_string = "Hello, World!"

# Accessing characters in a string
print(my_string[0])  # Output: H
print(my_string[-1])  # Output: !

# Slicing a string
print(my_string[7:12])  # Output: World

# Concatenating strings
greeting = "Hello"
name = "Alice"
message = greeting + ", " + name + "!"
print(message)  # Output: Hello, Alice!

# String methods
print(my_string.upper())  # Output: HELLO, WORLD!
print(my_string.lower())  # Output: hello, world!
print(my_string.replace("Hello", "Hi"))  # Output: Hi, World!

# String formatting
age = 25
print("I am {} years old.".format(age))  # Output: I am 25 years old.


# create me the basics of numbers in python

# Creating an integer
my_int = 7
print(my_int)  # Output: 7

# Creating a float
my_float = 1.23
print(my_float)  # Output: 1.23

# Creating a complex number
my_complex = 3 + 2j
print(my_complex)  # Output: (3+2j)

# Arithmetic operations
num1 = 12
num2 = 3
print(num1 + num2)  # Output: 15
print(num1 - num2)  # Output: 9
print(num1 * num2)  # Output: 36
print(num1 / num2)  # Output: 4.0
print(num1 // num2)  # Output: 4
print(num1 % num2)  # Output: 0

# Assignment operations
num = 10
num += 2
print(num)  # Output: 12
num *= 2
print(num)  # Output: 24

# Comparison operations
print(3 > 2)  # Output: True
print(3 < 2)  # Output: False
print(3 == 2)  # Output: False
print(3 != 2)  # Output: True

# Logical operations
print(3 > 2 and 3 < 5)  # Output: True
print(3 > 2 or 3 < 2)  # Output: True
print(not True)  # Output: False

